class basenumber {
	public static void main(String[] args){
		System.out.printf("%d\n", 255);	// 255	←☆10進数。
		System.out.printf("%d\n", 0b11111111);	// 255	←☆2進数。
		System.out.printf("%d\n", 0377);	// 255	←☆8進数。
		System.out.printf("%d\n", 0xff);	// 255	←☆16進数。
	}
}

